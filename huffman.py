def make_freq_dict(text):
    """ Return a dictionary that maps each byte in text to its frequency.

    @param bytes text: a bytes object
    @rtype: dict{int,int}

    >>> d = make_freq_dict(bytes([65, 66, 67, 66]))
    >>> d == {65: 1, 66: 2, 67: 1}
    True
    """
    # todo
    d = {}
    for w in text:
        if w in d:
            d[w] += 1
        else:
            d[w] = 1
    return d



def huffman_tree(freq_dict):
    """ Return the root HuffmanNode of a Huffman tree corresponding
    to frequency dictionary freq_dict.

    @param dict(int,int) freq_dict: a frequency dictionary
    @rtype: HuffmanNode

    >>> freq = {2: 6, 3: 4}
    >>> t = huffman_tree(freq)
    >>> result1 = HuffmanNode(None, HuffmanNode(3), HuffmanNode(2))
    >>> result2 = HuffmanNode(None, HuffmanNode(2), HuffmanNode(3))
    >>> t == result1 or t == result2
    True
    >>> freq2 = {'A': 10, 'E': 15, 'I': 12, 'S': 3, 'T': 4, 'P': 13, '/n': 1}
    >>> t2 = huffman_tree(freq2)
    >>> print(t2)
    test
    >>> freq_ = {1:64, 2:13, 3:22, 4:32, 5:103}
    >>> print(huffman_tree(freq_))
    test
    >>> freq_dict = {'a': 4, 'b': 1, 'c': 2, 'd': 2, 'e': 10}
    >>> print(huffman_tree(freq_dict))
    test
    """
    # put dictionary into list of tuples. tuple = (value, key)
    # reverse sort so items with lowest frequency is at bottom of tree
    f_sorted = sorted(freq_dict.items(), key=lambda x: x[1])
    f_sorted.reverse()
    nodes = []

    for item in f_sorted:
        nodes.append((HuffmanNode(item[0]), item[1]))

    while len(nodes) > 1:
        nodes = sorted(nodes, key=lambda x: x[-1])
        nodes.reverse()
        n = HuffmanNode(None)
        n.left = nodes[-1][0]
        f_left = nodes[-1][1]
        nodes.pop(-1)
        n.right = nodes[-1][0]
        f_right = nodes[-1][1]
        nodes.pop(-1)
        nodes.append((n, f_left + f_right))

    return nodes[0][0]

